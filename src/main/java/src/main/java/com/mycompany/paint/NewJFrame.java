    private void jpPaintMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jpPaintMouseReleased
        // TODO add your handling code here:
        if (tipoFigura == TipoFigura.tpRetangulo) {

            //r.corInterna = JColorChooser.showDialog(null, "Escolha uma cor: ", Color.BLACK);

            r.base = evt.getX() - r.x;
            r.largura = evt.getY()- r.y;
            r.cor = jpCor1.getBackground();
            r.corInterna = corAtual2;
            r.showArea = cbArea.getState();
            r.showPerimetro = cbPerimetro.getState();
            r.desenhar(jpPaint.getGraphics());
        }
        if(tipoFigura == TipoFigura.tpReta){
            reta.X1 = evt.getX();
            reta.Y1 = evt.getY();
            reta.cor = corAtual1;
            reta.showComprimento = cbComprimento.getState();
            reta.desenhar(jpPaint.getGraphics());
        }
        if(tipoFigura == TipoFigura.tpCirculo){
            circ.showArea = cbArea.getState();
            circ.showPerimetro = cbPerimetro.getState();
            circ.X2 = evt.getX();
            circ.Y2 = evt.getX();
            circ.cor = corAtual1;
            circ.corInterna = corAtual2;
            circ.raio = (evt.getX()-circ.x)/2;
            circ.desenhar(jpPaint.getGraphics());

        }
        if(tipoFigura == TipoFigura.tpCilindro){
            cilin.showArea = cbArea.getState();
            cilin.showVolume = cbVolume.getState();

            cilin.X2 = evt.getX();
            cilin.Y2 = evt.getY();

            cilin.cor = corAtual1;
            cilin.corInterna = corAtual2;

            cilin.raio = (evt.getX()-cilin.x)/2;

            cilin.desenhar(jpPaint.getGraphics());
        }
        if(tipoFigura == TipoFigura.tpPiramide){
            piram.showArea = cbArea.getState();
            piram.showVolume = cbVolume.getState();

            piram.X2 = evt.getX();
            piram.Y2 = evt.getY();

            piram.cor = corAtual1;
            piram.corInterna = corAtual2;

            piram.desenhar(jpPaint.getGraphics());
        }
        a.clear();
        b.clear();
    }//GEN-LAST:event_jpPaintMouseReleased

    private void jpPaintMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jpPaintMousePressed
        // TODO add your handling code here:

        if (tipoFigura == TipoFigura.tpPonto){
            p.x = evt.getX();
            p.y = evt.getY();
            p.cor = jpCor1.getBackground();
            p.desenhar(jpPaint.getGraphics());
        }
        if (tipoFigura == TipoFigura.tpRetangulo) {
            r.x = evt.getX();
            r.y = evt.getY();

        }
        if (tipoFigura == TipoFigura.tpReta){
            reta.x = evt.getX();
            reta.y = evt.getY();
        }
        if (tipoFigura == TipoFigura.tpCirculo){
            circ.x = evt.getX();
            circ.y = evt.getY();
        }
        if (tipoFigura == TipoFigura.tpPoligono){
            pol.cor = corAtual1;
            pol.corInterna = corAtual2;
            pol.adicionarPontos(evt.getX(), evt.getY());
            
            if( pol.listaPontos.size() >= 3 && evt.getButton() == MouseEvent.BUTTON2){
                pol.desenhar(jpPaint.getGraphics());
            }
        }
        if(tipoFigura == TipoFigura.tpCilindro){
            cilin.x = evt.getX();
            cilin.y = evt.getY();
        }
        if(tipoFigura == TipoFigura.tpPiramide){
            piram.x = evt.getX();
            piram.y = evt.getY();
        }
        /*if (tipoFigura == TipoFigura.tpSpray){
            sp.G = jpPaint.getGraphics();
            sp.cor = corAtual1;
            sp.min_valX = evt.getX()-50;
            sp.max_valX = evt.getX()+50;
            sp.min_valY = evt.getY()-50;
            sp.max_valY = evt.getY()+50;
            sp.desenhar();

        }*/
    }//GEN-LAST:event_jpPaintMousePressed

    private void jpPaintMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jpPaintMouseDragged
        if(tipoFigura == TipoFigura.tpBorracha){
            bo.x = evt.getX();
            bo.y = evt.getY();
            bo.cor = jpPaint.getBackground();
            bo.desenhar(jpPaint.getGraphics());
        }
        if(tipoFigura == TipoFigura.tpSpray){
            a.add(evt.getX());
            //b.add(evt.getY());
            sp.cor = corAtual1;

            for(int i =0 ; i< a.size()-1; i++){
                sp.G = jpPaint.getGraphics();
                sp.min_valX = evt.getX()-50;
                sp.max_valX = evt.getX()+50;
                sp.min_valY = evt.getY()-50;
                sp.max_valY = evt.getY()+50;
                sp.desenhar();
            }
        }
    }//GEN-LAST:event_jpPaintMouseDragged
    
    
    
